    public void getAllRecords(){
          try{
              
              switch(s1){
                      case "new_delhi":
                          jLabel1.setText("New Delhi");
                          break;
                          
                      case "south_delhi":
                          jLabel1.setText("South Delhi");
                          break;
                          
                      case "west_delhi":
                          jLabel1.setText("West Delhi");  
                          break;
                          
                      case "east_delhi":
                          jLabel1.setText("East Delhi");
                          break;
                          
                      case "north_west_delhi":
                          jLabel1.setText("North West Delhi");  
                          break;
                          
                      case "north_east_delhi":
                          jLabel1.setText("North East Delhi");  
                          break;
                          
                      case "chandni_chowk":
                          jLabel1.setText("Chandni Chowk");    
                          break;
              }
              
              DriverManager.registerDriver(new com.mysql.jdbc.Driver());
              Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/constituency?useSSL=false","pro","apple");
              
            
              PreparedStatement ps;
              ps=con.prepareStatement("select * from "+s1);
              
              ResultSet rs=ps.executeQuery();
            
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        }catch(Exception e)
        {
            e.printStackTrace();
        }
    }
